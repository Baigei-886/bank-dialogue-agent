add(24,"闲聊拒答",1,"今天天气怎么样","礼貌引回银行业务","银行|业务|帮您|协助","",1)

# 情绪
add(25,"情绪-愤怒",1,"你们什么破系统卡又锁了气死了",
    "先共情安抚再引导核验","理解|抱歉|歉意|别急|放心|尽快","",1)
add(26,"情绪-焦虑",1,"我妈住院急用钱卡锁了求你们快点",
    "先安抚再快速引导","理解|别急|放心|抱歉|尽快|马上","",1)

df = pd.DataFrame(rows)
out = os.path.join(_DATA, "testset.xlsx")
