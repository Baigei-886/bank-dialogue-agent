# 生成批量测评集。核验/转账类用例从当前虚拟库里挑真实存在的行，
# 保证测试数据与库同步（库是随机生成的，这里跟着走，不写死具体值）。
# 列：用例编号/测试类型/对话轮次/输入内容/标准答案/期望包含/禁止包含/判分轮次
import os
import pandas as pd

_DATA = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "data")
cust = pd.read_excel(os.path.join(_DATA, "customers.xlsx"), dtype=str)
tran = pd.read_excel(os.path.join(_DATA, "transfers.xlsx"), dtype=str)

def pick_cust(status):
    r = cust[cust["账户状态"] == status].iloc[0]
    return r["姓名"], r["卡号后4位"], r["注册手机号"]

def pick_tran(status):
    r = tran[tran["转账状态"] == status].iloc[0]
    return r["账号后4位"], r["转账金额"]

# 从库里挑代表性数据
lk_name, lk_card, lk_phone = pick_cust("已锁定")
